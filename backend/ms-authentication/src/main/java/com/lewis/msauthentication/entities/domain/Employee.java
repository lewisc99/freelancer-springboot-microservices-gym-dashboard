package com.lewis.msauthentication.entities.domain;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.ArrayList;
import java.util.Collection;
import java.util.List;
import java.util.UUID;

public class Employee implements UserDetails {
    private UUID id;
    private String username;
    private String email;
    private String password;
    private boolean active = true;

    private List<Roles> roles =  new ArrayList<>();

    public Employee(){}

    public Employee(UUID id, String username, String email, String password) {
        this.id = id;
        this.username = username;
        this.email = email;
        this.password = password;
    }

    public UUID getId() {
        return id;
    }

    public void setId(UUID id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }


    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }



    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {


        return checkGrantAuthorities();
    }
    private List<GrantedAuthority> checkGrantAuthorities() {


        List<GrantedAuthority> authoritiesRole = new ArrayList<GrantedAuthority>();

        if(username!=null && getRoles()!=null && getRoles().isEmpty()==false)
            for(Roles roleEmployee : getRoles()){


                authoritiesRole.add(new SimpleGrantedAuthority(roleEmployee.getName()));
            }

        return authoritiesRole;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public boolean isActive() {
        return active;
    }

    public void setActive(boolean active) {
        this.active = active;
    }

    public List<Roles> getRoles() {
        return roles;
    }

    public void setRoles(List<Roles> roles) {
        this.roles = roles;
    }
}
